"""
api_runner.py -- headless NP-Hard Pac-Man runner for LLM benchmarking.

This script lets AI models play the NP-Hard Pac-Man game with no graphical
interface. Each move, the model receives an ASCII grid + explanation as a text
prompt; the model answers with one word: UP / DOWN / LEFT / RIGHT.

Supported providers
-------------------
  Claude   (Anthropic)  ->  ANTHROPIC_API_KEY
  GPT      (OpenAI)     ->  OPENAI_API_KEY
  DeepSeek              ->  DEEPSEEK_API_KEY
  Gemini   (Google)     ->  GOOGLE_API_KEY
  Kimi     (Moonshot)   ->  MOONSHOT_API_KEY

Usage
-----
  # One model, one level
  python api_runner.py --model claude-opus-4-7 --level 0

  # Multiple runs for statistics
  python api_runner.py --model deepseek-chat --level 0 --runs 5

  # Compare all models on one level
  python api_runner.py --all-models --level 0 --runs 3

  # All models on all levels
  python api_runner.py --all-models

Output
------
  - Live move log in the terminal (position, direction, result)
  - One JSON log file per run in logs/ with the full model reasoning
  - Comparison table (WIN%, AVG_MOVES, AVG_TOKENS) with --all-models

Architecture of a single run
----------------------------
  1. make_state(lvl)           ->  initialise the game state
  2. state_to_prompt(...)      ->  build ASCII grid + rule explanation as text
  3. call_model(model, prompt) ->  send to the API, receive reasoning + move
  4. parse_move(response)      ->  extract UP/DOWN/LEFT/RIGHT from the answer
  5. apply_move(move, ...)     ->  apply the move and move the ghost(s)
  6. Repeat until status != "playing"
  7. Save the JSON log in logs/
"""

