# ── Gap scoring (identical to human scoring in level1.py) ────────────────────
_GAP_REWARDS  = {0: 50, 1: 40, 2: 30, 3: 20, 4: 10}   # gap → coins (5+ = 0)
_GAP_RANGES   = ["exact", "±1", "±2", "±3", "±4", "±5+"]
_GAP_BONUS    = 50   # extra coins if estimate matches actual gap

def _gap_reward(gap):
    return _GAP_REWARDS.get(gap, 0)

def _parse_estimate(text):
    """Extract gap estimate from model response. Returns int bucket: 0=exact,1,2,3,4,5=±5+"""
    t = text.lower()
    if re.search(r"\bexact\b|±\s*0\b|off by 0|\b0 move", t):
        return 0
    for n in [5, 4, 3, 2, 1]:
        if re.search(rf"±\s*{n}\b|off by {n}|\b{n} move", t):
            return n
    if re.search(r"±\s*5\+|5 or more|5\+", t):
        return 5
    return None


def run_estimate_level(lvl_idx, model, verbose=True, run_idx=1, log_dir="logs_wager"):
    """Run one estimate level (4C/4D): play without move limit, then ask for gap estimate.

    Flow:
      1. Show model the exact same instructions humans see (no move limit, estimate after).
      2. Play the level normally (max_moves=9999 so no cutoff).
      3. After completion, show moves_used and ask: how far off from optimal?
         Options: Exact / ±1 / ±2 / ±3 / ±4 / ±5+  (same six ranges as humans).
      4. Score: gap_reward (based on actual gap) + guess_bonus if estimate correct.
      5. Save JSON log.
    """
    lvl        = LEVELS[lvl_idx]
    state      = make_state(lvl)
    optimal    = lvl.get("optimal_moves", 0)

    os.makedirs(log_dir, exist_ok=True)
    safe_model = model.replace("/", "-")
    safe_level = lvl["title"].replace(" ", "-")
    timestamp  = time.strftime("%Y%m%d_%H%M%S")
    log_path   = os.path.join(log_dir, f"{safe_model}_{safe_level}_run{run_idx}_{timestamp}.json")

    is_claude  = _provider(model) == "anthropic"

    # System prompt: include the human estimate-game instructions verbatim
    win_condition = "Collect ALL bananas (B)"
    if lvl.get("require_ghost_eaten"):
        win_condition += " AND eat ALL ghosts (G) — requires powerup (P)"
    estimate_instructions = f"""ESTIMATE GAME — LEVELS 4C & 4D
════════════════════════════════

HOW IT WORKS
────────────
You will now play an estimate level.

Win condition: {win_condition}.
There is no move limit — take as many moves as you need to complete the level.
The goal is to complete the level as efficiently (close to optimal) as possible.

AFTER COMPLETING THE LEVEL
──────────────────────────
You will be told how many moves you used.
You must then estimate how far off you were from the optimal (fewest possible) moves.

Choose one of six ranges:
  Exact  /  ±1  /  ±2  /  ±3  /  ±4  /  ±5 or more

COINS
─────
Gap reward  (always earned, based on actual gap from optimal):
  Exact=50   ±1=40   ±2=30   ±3=20   ±4=10   ±5+=0 coins

Guess bonus  (only if your chosen range matches the actual gap):
  +50 coins extra if your estimate was correct.

STRATEGY: play as efficiently as possible to maximise your gap reward.
Keep track of your moves so you can estimate your gap accurately afterwards."""

    sys_prompt = build_system_prompt(claude=is_claude) + "\n\n" + estimate_instructions

    if verbose:
        print(f"\n{'='*60}")
        print(f"  {lvl['title']}  [{model}]  ESTIMATE LEVEL")
        print(f"  Optimal: {optimal}  |  No move limit")
        print(f"{'='*60}")

    total_input_tokens  = 0
    total_output_tokens = 0
    moves_log           = []
    blocked_combos      = set()

    # Capture the initial grid+legend so the estimate prompt can show the full layout.
    initial_grid_block = state_to_prompt(lvl, state, moves_log=None, implicit=False)
    initial_grid_block = initial_grid_block.replace("\n\nChoose your next move.", "").rstrip()

    run_log = {
        "model":     model,
        "level":     lvl["title"],
        "level_idx": lvl_idx,
        "run":       run_idx,
        "moves":     [],
    }

    def _save_log():
        with open(log_path, "w", encoding="utf-8") as f:
            json.dump(run_log, f, indent=2, ensure_ascii=False)

    _save_log()

    # ── Play the level (no move limit) ───────────────────────────────────────
    move_num = 0
    while state["status"] == "playing":
        base_prompt = state_to_prompt(lvl, state, moves_log, implicit=False)
        move_num += 1

        t0 = time.time()
        try:
            response_text, in_tok, out_tok, reasoning = call_model(
                model, base_prompt, system_prompt=sys_prompt, max_tokens=4096*3)
        except KeyboardInterrupt:
            print("\n  [INTERRUPTED]")
            break
        except Exception as e:
            if verbose:
                print(f"  [API ERROR move {move_num}] {e}")
            break
        response_time = round(time.time() - t0, 2)

        total_input_tokens  += in_tok
        total_output_tokens += out_tok

        move_str   = parse_move(response_text)
        pos_before = f"{col_label(state['col'])}{row_label(state['row'])}"
        result     = apply_move(move_str, lvl, state) if move_str else "invalid"
        pos_after  = f"{col_label(state['col'])}{row_label(state['row'])}"

        blocked = result == "blocked"
        combo   = (pos_before, move_str)
        perseveration = blocked and combo in blocked_combos
        if blocked:
            blocked_combos.add(combo)

        moves_log.append({
            "move_num":   move_num,
            "pos_before": pos_before,
            "pos_after":  pos_after,
            "move":       move_str or "?",
            "result":     result,
        })

        if not reasoning:
            move_match = re.search(r"MOVE\s*:\s*(UP|DOWN|LEFT|RIGHT)", response_text, re.IGNORECASE)
            if move_match:
                reasoning = response_text[:move_match.start()].strip()

        log_entry = {
            "move_num":      move_num,
            "pos_before":    pos_before,
            "pos_after":     pos_after,
            "move":          move_str or "?",
            "result":        result,
            "moves_left":    state["moves_left"],
            "perseveration": perseveration,
            "response_time": response_time,
            "tokens_in":     in_tok,
            "tokens_out":    out_tok,
            "reasoning":     reasoning or "",
            "full_response": response_text,
        }
        run_log["moves"].append(log_entry)

        if verbose:
            icon = "✓" if result not in ("blocked", "invalid") else "✗"
            print(f"  Move {move_num:2d}: {move_str or '?':5s} {icon} {result:12s}  "
                  f"pos={pos_after}  moves_used={move_num}  ({response_time}s)")

        if os.path.exists("STOP"):
            with open("STOP", "r", encoding="utf-8") as _sf:
                _stop_reason = _sf.read().strip()
            os.remove("STOP")
            state["status"] = "not_completable"
            state["reason"] = _stop_reason or "level not completable anymore"
            print(f"\n  [ABORTED] STOP file — {state['reason']}")
            break

        _save_log()
        time.sleep(0.05)

    # ── Ask for gap estimate ──────────────────────────────────────────────────
    status     = state["status"]
    moves_used = move_num
    won        = status == "level_complete"

    estimate_bucket = None
    estimate_text   = ""
    estimate_tokens = 0
    e_in = e_out = 0

    if won:
        gap_actual = max(0, moves_used - optimal)

        estimate_prompt = (
            f"You completed {lvl['title']} in {moves_used} moves.\n\n"
            f"Here is the level layout (as it was at the start):\n"
            f"{initial_grid_block}\n\n"
            f"Here is your full move history:\n"
            f"{move_memory_prompt(moves_log, max_recent=len(moves_log))}\n\n"
            f"Review the grid and your moves. Identify any wasted moves (blocked moves, detours, backtracking).\n"
            f"Based on this, estimate how far off you were from the optimal (fewest possible) number of moves.\n\n"
            f"Choose exactly one of these six options:\n\n"
            f"  Exact    — you used exactly the optimal number of moves\n"
            f"  ±1       — you were 1 move off\n"
            f"  ±2       — you were 2 moves off\n"
            f"  ±3       — you were 3 moves off\n"
            f"  ±4       — you were 4 moves off\n"
            f"  ±5+      — you were 5 or more moves off\n\n"
            f"Think about how efficiently you played, then end your response with exactly:\n"
            f"ESTIMATE: <your choice>\n\n"
            f"Example: ESTIMATE: ±2"
        )

        try:
            est_text, e_in, e_out, est_reasoning = call_model(
                model, estimate_prompt, system_prompt=sys_prompt, max_tokens=15000)
            estimate_tokens = e_in + e_out
            estimate_text   = est_text
            total_input_tokens  += e_in
            total_output_tokens += e_out

            m = re.search(r"ESTIMATE\s*:\s*(.+)", est_text, re.IGNORECASE)
            if m:
                estimate_bucket = _parse_estimate(m.group(1).strip())
            if estimate_bucket is None:
                estimate_bucket = _parse_estimate(est_text)

            if verbose:
                print(f"\n  Estimate response: {est_text.strip()[:100]}")
                label = _GAP_RANGES[min(estimate_bucket, 5)] if estimate_bucket is not None else "?"
                print(f"  Estimate: {label}  |  Actual gap: {gap_actual}")
        except Exception as e:
            if verbose:
                print(f"  [ESTIMATE API ERROR] {e}")

        # Score
        gap_reward   = _gap_reward(gap_actual)
        guess_correct = estimate_bucket is not None and estimate_bucket == min(gap_actual, 5)
        guess_bonus  = _GAP_BONUS if guess_correct else 0
        total_reward = gap_reward + guess_bonus
        guess_label  = _GAP_RANGES[min(estimate_bucket, 5)] if estimate_bucket is not None else "?"

        if verbose:
            icon = "✓" if guess_correct else "✗"
            print(f"  Gap reward: +{gap_reward}  |  Guess bonus: +{guess_bonus}  "
                  f"|  Total: +{total_reward}  {icon}")
    else:
        gap_actual    = None
        gap_reward    = 0
        guess_correct = False
        guess_bonus   = 0
        total_reward  = 0
        guess_label   = "—"

    total_tokens = total_input_tokens + total_output_tokens

    if verbose:
        icon = "WIN" if won else "FAIL"
        print(f"\n  [{icon}]  Moves used: {moves_used}  (optimal: {optimal})")
        print(f"  Tokens: {total_tokens:,}  (in={total_input_tokens:,} out={total_output_tokens:,})")
        print(f"  Log: {log_path}")

    run_log.update({
        "status":          status,
        "won":             won,
        "moves_used":      moves_used,
        "optimal_moves":   optimal,
        "gap_actual":      gap_actual,
        "gap_reward":      gap_reward,
        "guess_label":     guess_label,
        "guess_correct":   guess_correct,
        "guess_bonus":     guess_bonus,
        "total_reward":    total_reward,
        "estimate_text":   estimate_text,
        "estimate_tokens": estimate_tokens,
        "total_input_tokens":  total_input_tokens,
        "total_output_tokens": total_output_tokens,
        "coins_final":     total_reward,
    })
    _save_log()
    return run_log

