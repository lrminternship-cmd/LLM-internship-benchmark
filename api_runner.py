    if state["powered_turns"] > 0:
        status_lines.append(f"Power-up active for {state['powered_turns']} more move(s)")

    if not implicit:
        if state["delayed_pu_taken"] and not state["powered_turns"]:
            if state["delayed_pu_in_zone"]:
                status_lines.append("Delayed powerup: countdown PAUSED — leave the zone to start it")
