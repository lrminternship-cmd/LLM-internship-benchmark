        ambush_done   = not lvl.get("ambush_ghost") or state.get("ambush_ghost") is None
        ghosts_eaten  = (1 if patrol_done and lvl.get("ghost") else 0) + (1 if ambush_done and lvl.get("ambush_ghost") else 0)
