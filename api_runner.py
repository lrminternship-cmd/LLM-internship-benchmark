def _is_reasoner(model_id):
    return model_id in _REASONER_MODELS

# API costs in USD per 1 million tokens (verified April 2026).
# Input = tokens sent to the model (prompt + system prompt).
# Output = tokens generated by the model (reasoning + answer).
