def _result_row(results, model, lvl_title, n):
    wins       = [r for r in results if r["status"] == "level_complete"]
    win_pct    = f"{len(wins)*100//n}%"
    avg_moves  = f"{sum(r['moves_used'] for r in wins)/max(len(wins),1):.1f}" if wins else "—"
    avg_tokens = f"{sum(r['tokens'] for r in results)/n:,.0f}"
    return f"  {model:<22} {win_pct:<7} {avg_moves:<12} {avg_tokens}"


