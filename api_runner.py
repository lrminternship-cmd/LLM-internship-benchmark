def state_to_prompt(lvl, state, moves_log=None, implicit=False):
    cols, rows = lvl["cols"], lvl["rows"]
    broken     = state.get("broken_walls", set())
    used_gw    = state.get("used_green_walls", set())
    lrz        = lvl.get("local_rule_zone")
    stps       = lvl.get("shared_teleporters")

    # ── Zone cell set (for · marker) ─────────────────────────────────────────
    zone_cells = set()
    if lrz:
        c_min, r_min, c_max, r_max = lrz["rect"]
        for zc in range(c_min, c_max + 1):
            for zr in range(r_min, r_max + 1):
                zone_cells.add((zc, zr))

    # ── Build grid (empty zone cells shown as ·) ──────────────────────────────
    grid = [["·" if (c, r) in zone_cells else "." for c in range(cols)] for r in range(rows)]

    for g in state["goals"]:
        if not g.get("collected"):
            gc, gr = g["pos"]
            grid[gr][gc] = "B"

    for b in state["boxes"]:
        grid[b[1]][b[0]] = "X"
    for b in state["meta_boxes"]:
        grid[b[1]][b[0]] = "M"

    gate = state["gate"]
    if gate:
        gc, gr = gate["pos"]
        grid[gr][gc] = "O" if gate["open"] else "o"

    powerups = lvl.get("powerups") or ([lvl["powerup"]] if lvl.get("powerup") else [])
    for pi, pu in enumerate(powerups):
        if pi not in state["powerups_taken"]:
            grid[pu["pos"][1]][pu["pos"][0]] = "P"

    dpu = lvl.get("delayed_powerup")
    if dpu and not state["delayed_pu_taken"]:
        grid[dpu["pos"][1]][dpu["pos"][0]] = "D"

    tps = lvl.get("teleporters")
    if tps:
        grid[tps[0][1]][tps[0][0]] = "T"
        grid[tps[1][1]][tps[1][0]] = "T"

    if stps:
        grid[stps[0][1]][stps[0][0]] = "S"
        grid[stps[1][1]][stps[1][0]] = "S"

    gtps = lvl.get("ghost_teleporters")
    if gtps:
        grid[gtps[0][1]][gtps[0][0]] = "Q"
        grid[gtps[1][1]][gtps[1][0]] = "Q"

    if state["ghost"] and not state["ghost_eaten"]:
        g = state["ghost"]
        grid[g["row"]][g["col"]] = "G" if state["powered_turns"] == 0 else "g"
    if state.get("ambush_ghost"):
        ag = state["ambush_ghost"]
        sym = "A" if ag["activated"] else "a"
        if state["powered_turns"] > 0:
            sym = sym.lower()
        grid[ag["row"]][ag["col"]] = sym

    grid[state["row"]][state["col"]] = "@"

